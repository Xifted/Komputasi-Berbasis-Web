        <p class="mt-4">
            Kemacetan horor di Jalur Puncak Bogor, Jawa Barat, saat libur panjang 14-16 September 2024 disebabkan volume
            kendaraan motor meningkat serta banyak yang melambung saat diberlakukan rekayasa lalulintas one way (satu
            arah). "Pelambungan-pelambungan dari roda dua, di mana yang turun dari puncak dan naik sama besarnya,
            akhirnya mereka bertemu di satu titik mengakibatkan hambatan (kemacetan)," kata Kasat Lantas Polres Bogor
            AKP Rizky Guntama, Selasa (17/9/2024). Di sisi lain, kemacetan saat libur panjang juga terjadi karena
            meningkatnya mobilitas masyarakat atau wisatawan yang menuju Puncak Bogor. Ia tak memungkiri banyak
            pengendara yang melewati jalur alternatif untuk bisa sampai ke tempat tujuan (ke atas). Jalur alternatif itu
            meliputi Pasir Muncang, Simpang Megamendung, dan Pasar Cisarua. Hal itu juga yang kemudian menambah
            kemacetan di jalur-jalur alternatif atau jalan perkampungan tersebut.
        </p>
        <p>Kemacetan akan kembali diperparah saat mereka sudah masuk menuju jalan utama. Motor-motor tersebut akan
            melambung sehingga memakan sebagian badan jalan. Alhasil, laju kendaraan lain atau yang naik maupun turun
            menjadi terhambat di Jalan Raya Puncak. Kemacetan panjang pun tak terelakkan lagi. "Adanya hambatan-hambatan
            di mana roda dua banyak melambung memakan jalur one way yang harusnya bisa tiga lajur naik ke atas, menjadi
            dua lajur," ujarnya. Rizky mengaku, saat itu anggota sudah diploting di titik-titik hambat untuk mengatur
            rekayasa lalin one way. Bahkan, personil dari polres maupun instansi terkait juga ditambah agar semua ruas
            jalan terakomodir. Namun karena meningkatnya volume kendaraan yang mengarah ke Puncak Bogor, kemacetan belum
            dapat teratasi hingga tengah malam.
        </p>
        <p>
            Keesokan harinya, petugas langsung menerapkan one way ke bawah lebih awal atau pagi hari untuk mengatasi
            kemacetan. Berkat kerja keras petugas, kemacetan lalu lintas akhirnya dapat terurai pada Senin (16/9/2024)
            petang. "Didominasi roda dua, di mana teman-teman kita (wisatawan) yang berlibur kemarin naik menggunakan
            roda dua. Kami sudah berinisiatif untuk memberikan prioritas penuh agar yang kemarin tersendat dapat terurai
            semuanya," ujar dia. Selama libur panjang itu, polisi mencatat total ada 391.142 kendaraan yang melintasi
            jalur puncak. Didominasi roda dua. Adapun jumlah tersebut telah melebihi daya tampung Jalan Raya Puncak
            Bogor. Kondisi itu juga yang menyebabkan kemacetan.
        </p>
