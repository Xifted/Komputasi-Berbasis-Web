        <div class="d-flex justify-content-center align-items-center">
            <img src="https://asset.kompas.com/crops/wjLaQi4tzynTHsFCLD_c3PZftYg=/7x0:597x393/750x500/data/photo/2024/09/16/66e778ae626b7.jpg"
